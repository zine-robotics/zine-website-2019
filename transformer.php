        <h1>Introduction</h1><br>
        Transformers are devices used to transfer electrical energy in the form of magnetic energy, between two or more
        coils of wire. During this transfer, we can regulate the levels of current and voltage, according to our needs.
        Put more formally, <br><br>

        <i>A transformer is a very simple static electro-magnetic passive electrical device that works on the principle
            of Faraday’s Law of Electromagnetic Induction by converting electrical energy from one form to another.
        </i><br><br>

        Whoa! Too many terms at once. Let us try to understand this definition step by step. It is “static” i.e
        stationary or immovable. The principle is based on the conversion of electrical to magnetic energy, hence
        “electromagnetic”. Recall, we talked about what is a “passive” element earlier. <br><br>

        <img src="img/images/transformer/image7.gif" class="img-responsive floating-image right">
