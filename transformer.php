        <h1>Autotransformer</h1><br>
        <img src="img/images/transformer/image8.jpg" class="img-responsive floating-image left"><br><br>
        No discussion about transformers can call itself complete without the autotransformer. An autotransformer is a
        special type of transformer with only a single winding. <br><br>
        How could a transformer work with just a single winding !? Let's find out. <br><br>
        In an autotransformer, the winding is tapped at various points along its length to provide a portion of the
        primary voltage to the secondary circuit. The main advantage of this design is that it is more economical with
        the same rating as the conventional transformer. <br><br>
        <h2>Uses</h2>
        Equipped with all this knowledge we can now fully understand and appreciate the vital role played by a
        transformer in electrical circuits. <br>
        <ul style="list-style-type:circle">
            <li>Their primary task is to raise or lower voltages according to need. In transmission higher voltages are
                required as they incur fewer losses(from P=I2*R) and hence have an economic benefit. So, before
                transmission from power grids, voltages are stepped up using transformers. However, for domestic use,
                lower voltages are required. Hence, voltages are stepped down using a transformer before supplying for
                domestic use. The generality of this use may help you understand the scale at which transformers are
                used. <br></li>
            <li>We talked about how the components of a transformer are electrically insulated. This is put to use for
                electrically isolating two circuits.
                <br></li>
            <li>We have one last application of transformers. Transformers are also sometimes used to prevent DC current
                from passing from one circuit to another.
