        the construction of a transformer.
        <br>
        <br><br><br>
        <h2>Construction</h2>
        <br>
        <img src="img/images/transformer/image5.gif" class="img-responsive floating-image center">
        <br><br>
        The construction of transformers is done in two different ways. There is the shell-type transformer and the
        core-type transformer. <br><br>
        We’ll discuss them one by one. But before that let’s go through some points common to both. A basic transformer
        has three parts - a primary coil, a secondary coil, and a laminated steel core. The core is formed by stacking
        multiple sheets of steel on top of one another. All parts are electrically insulated from each other. <br><br>
        <img src="img/images/transformer/image11.gif" class="img-responsive floating-image center">
        Why is steel used, can’t we use other materials? What is the need for stacking the sheets of steel, wouldn’t
        making a solid core be much easier? <br>
        A lot of questions may have popped up in your mind. Let’s find some answers. The reason behind all of these has
        something to do with losses. In a transformer, there are mainly two kinds of losses - hysteresis loss and eddy
        current loss. Hysteresis loss is caused by the cyclic reversal of magnetic fields that occurs in our circuit.
        And eddy current loss occurs due to heat dissipation caused by the currents induced in the core due to the same
        fields. These will get more apparent once we get into the actual working of a transformer. For now, understand
        that hysteresis loss depends on the material and hence steel is used. As for eddy currents, the magnitude of the
        current induced is proportional to the length of the conductor perpendicular to the magnetic field. Therefore,
        using thin laminated sheets reduces the current induced, and consequently the heat loss. <br>
        Now we can move to the two types of transformers. <br>
        <img src="img/images/transformer/image10.png" class="img-responsive floating-image center">
        The horizontal pillars of the core are called yokes, whereas the vertical ones are called limbs.
        <br>
        <h3>Core Types</h3>
        In a core type transformer, the primary and secondary coils are wound outside and surrounding the core ring. For
        better transfer of energy, sometimes the windings are divided into parts with one part of both wrapped on one
        limb of the core and other parts on the other. Core type transformers are generally used for high voltage
        applications.
        <img src="img/images/transformer/image6.jpg" class="img-responsive floating-image center">
        <h3>Shell Types</h3>
        On the other hand, in a shell type transformer the primary and secondary coils are wound in the interior of the
        core and it forms a shell-like structure around them. These are generally used for low voltage applications.
        <br>
        <img src="img/images/transformer/image16.png" class="img-responsive floating-image center"><br><br>
        We can now finally move to the working principle of a transformer.
        <br><br>
        <h3>Working Principle</h3><br>
        <img src="img/images/transformer/image12.gif" class="img-responsive floating-image center"><br><br>
        The working of a transformer is pretty easy to understand. It starts when an AC voltage is introduced across the
        primary coil. The current produces a magnetic field that links with the entire steel core. This magnetic field
        varies with time as our voltage. Now recall Faradays’ law. This varying magnetic field will induce a current in
        the secondary coil. This is the functioning of the transformer. To summarize, there is the transfer of
        electrical energy between two coils in the form of magnetic energy.
        <br><br>
        Now, we mentioned that a transformer can “regulate the levels of voltage and current”. How does that happen?
        This is done by varying the number of turns in the primary and secondary windings. The magnetic flux created in
        the core is linked to both coils, hence if one coil has a different number of turns than the other, its voltage
        and current will also be different. The coil with greater number of turns will have higher voltage and lesser
        current. With this we can introduce the concept of step up and step down transformers.
        <br>
        <img src="img/images/transformer/image3.gif" class="img-responsive floating-image center"><br><br>
        <br>
        So, if the primary coil has more turns than the secondary coil then the primary voltage will be greater than the
        secondary voltage. This type of transformer is called a Step-Down transformer. And if the primary coil has fewer
        turns the transformer is called a Step-Up transformer.
        <img src="img/images/transformer/image1.png" class="img-responsive floating-image center"><br><br>
        <br>
        where Np, Ns is the number of turns of the primary and secondary coils respectively and Vp, Vs their voltages.
