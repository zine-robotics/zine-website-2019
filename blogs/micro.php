<div class="blogPageSubHeading">Components of a microcontroller
</div>
<br><br>
<img src="../img/images/micro/image6.png" class="img-responsive floating-image center">
<br><br>
<b>CPU: </b>  It is the processing unit of any computer. It is the “brains” of the microcontroller. It is what fetches the instructions from the code memory and executes the instructions that it fetches.
<br>
<b>RAM: </b> The data RAM (Random Access Memory) is the data space that is used for temporarily storing constant and variable values that are used by the microcontroller during normal program execution. 
<br>
<b>EEPROM: </b> The on-chip EEPROM memory (Electrically Erasable Programmable Read-Only Memory) on a microcontroller is like a microcontroller’s hard drive. It has two partitions. One partition is reserved for the storage of the program code i.e program memory while the other partition is reserved for permanent storage of data i.e data memory, used by the chip during normal program execution.
<br>
<b>I/O ports:</b>These are the ports that are used to provide binary data to the microcontroller and to fetch processed binary data from the microcontroller. They provide a channel to communicate with the microcontroller.
<br>
<b>Timers and counters: </b>Counters are simply used to generate a count in electronics such as 1,2,3,4. You also must know timers from the description where the 555 timer IC is described. In similar ways, microcontrollers are embedded with such components that act as timers and counters inside the microcontroller to perform time and count related tasks.
<br>
<b>Oscillators: </b>An oscillator embed in a microcontroller is used to provide a clock to the microprocessor of a microcontroller. In a microprocessor, every instruction is executed in synchronization with the clock. To achieve high clock speeds, an external oscillator can be used with a microcontroller.
<br>
<b>A/D Converters(Analog to Digital Converters and Digital to Analog Convertors) </b>Any microprocessor needs data to be in digital form i.e data has to be in a binary state to be processed. In microcontrollers, the CPU too requires digital data to work on but the input provided by devices is analog signals of voltage. So data needs to be converted into digital signals so analog to digital converters is present in a microcontroller to aid the processing. 
<br><br>
After data gets processed according to the instructions, the processed data in digital form is again needed to be converted into analog signals as most of the output devices need analog signals to show output. So, a digital to analog converter is also embedded inside a microcontroller for this purpose. Remember, there are external converters also available.
<br><br>
<div class="blogPageSubHeading">Classification of MCUs</div> <br>
Microcontrollers are classified into various categories based on memory, architecture, bits, and instruction sets. Following is the list of their types −
<br><br>
<u><b>1. On the basis of the number of bits</b></u>
<br><br>
Based on the number of bits microcontroller can process in one clock cycle, the microcontroller is further classified into three categories:
<br>
<u>a. 8-bit microcontroller: </u>Microcontrollers that process 8-bit of information in one clock cycle. This type of microcontroller is used to execute arithmetic and logical operations like addition, subtraction, multiplication division, etc. For example, Intel 8031 and 8051 are 8 bits microcontrollers.
<br>
<u>b. 16-bit microcontroller: </u>Microcontrollers that process 16-bit of information in one clock cycle. This type of microcontroller is used to perform arithmetic and logical operations where higher accuracy and performance are required. For example, Intel 8096 is a 16-bit microcontroller.
<br>
<u>c. 32-bit microcontroller: </u>Microcontrollers that process 32-bit of information in one clock cycle. This type of microcontroller is generally used in automatically controlled appliances like automatic operational machines, medical appliances, etc.
<br><br>
<u><b>2. On the basis of memory</b></u>
<br><br>
Based on the memory configuration, the microcontroller is further divided into two categories:
<br><br>
<u>a. External memory microcontroller: </u>This type of microcontroller is designed in such a way that it does not have a program memory on the chip. Hence, it is named an external memory microcontroller. For example, Intel 8031 microcontroller.
<br>
<u>b. Embedded memory microcontroller: </u>This type of microcontroller is designed in such a way that the microcontroller has all programs and data memory, counters, and timers interrupt, I/O ports are embedded on the chip. For example, Intel 8051 microcontroller.
<br>
<u><b>2. On the basis of Memory Architecture</b></u>
<br><br>
Based on the memory architecture on which microcontroller is created, they can be classified into two categories:
<br>
<u>a. Based on Von Neumann Memory Architecture: </u>These Microcontrollers have common memory for storage of data as well as programs.
<br>
<u>b. Based on Harvard Memory Architecture:</u>These Microcontrollers have separate memory units (and separate <a href="https://www.techopedia.com/definition/6733/data-bus#:~:text=A%20data%20bus%20is%20a,and%20other%20pieces%20of%20hardware.">buses</a>) for storage of data as well as programs.
<br><br><br>
Some of the examples of microcontrollers are ATmega328, ATtiny85, ESP8266, etc. <br>
