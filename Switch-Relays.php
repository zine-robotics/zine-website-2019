     <strong> What is a Switch? </strong>
     <br /> <br /> 
A <strong>Switch</strong> is a device that breaks(sometimes divertsüôÑ) the flow of current in a circuit. Simple enough?
<img src="./images/Switch and Relays/image14.png" class="img-responsive floating-image center"><br />

This is how we typically represent a switch. But wait. What are these <strong>1P</strong> and <strong>1T</strong> representing here?ü§î Told you already that switch is an interesting topic too.üòÄ
<strong>P</strong> represents <strong>Pole</strong> and <strong>T</strong> represents <strong>Throw</strong>. What are these? Let‚Äôs see.
<br /> <br /> 
<strong>Pole-</strong> The number of poles on a switch tells how many circuits it controls. For Example- A single-Pole switch controls only one circuit while a Double-Pole switch controls two independent circuits.
<br /> <br /> 
<strong>Throw-</strong> The number of throws indicates the number of output connections every single pole can connect to or the number of paths every pole provides for current to pass. Single throw means every pole provides only one path.
<br /> <br /> 
Now that you know what the switch is, let‚Äôs see how they are classified.
<br /> <br /> 
Switches are broadly classified into two types:<br /> 
1.Mechanical <br /> 
2.Electronic <br /> 
