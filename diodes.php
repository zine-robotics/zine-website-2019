        In simple words, a solar cell is an electronic device that directly converts sunlight into electricity. Light shining on the solar cell produces both a current and a voltage to generate electric power. Solar cells are generally manufactured from crystalline silicon.<br><br>
        <img src="images/diodes/image16.gif" class="img-responsive floating-image center"><br><br>
        The basic steps in the operation of a solar cell are:<br><br>
        <ol>
            <li>Generation of light-generated carriers: The light energy, in the form of photons, supplies sufficient energy to the junction to create a number of electron-hole pairs.<br><br></li>
            <li>The free electrons in the depletion region can quickly come to the n-type side of the junction. Similarly, the holes in the depletion can quickly come to the p-type side of the junction. This happens due to the electric field in the depletion region.<br><br></li>
            <li>As the concentration of electrons becomes higher in one side, i.e. n-type side of the junction, and the concentration of holes becomes more in another side, i.e. the p-type side of the junction, you will be able to observe a potential difference which is called photovoltage.<br><br></li>
            <br><br>
        </ol>

