<div class="blogPageHeading">Controlling the motion of the flight in aircraft </div><br><br>

The directional control of a fixed-wing aircraft takes place around the lateral, longitudinal, and vertical axes employing flight control surfaces designed to create a movement about these axes. These  <strong>control</strong> surfaces are generally movable and hinged objects.
<br> There are three axes along which we need to control our aircraft, let’s jump in and understand how manoeuvring is done. Take axes as imaginary lines along which the body can rotate.
<br><br>
<strong>Elevation of an aircraft</strong>, also known as Pitch, is controlled by <strong>elevators</strong>. As the name implies, the elevator helps “elevate” the aircraft. It is usually located on the tail of the aircraft and serves two purposes. The first is to provide stability by producing a downward force on the tail. Aeroplanes are traditionally nose-heavy (to improve stability from turbulence) and this downward force is required to compensate for that. The second is to direct the nose of the aircraft either upwards or downwards, known as pitch, to make the aeroplane climb and descend.    
                               
<br>
<img src="../img/images/aero/image5.png" class="img-responsive floating-image center">  

Next is <strong> rolling</strong>, done by <strong>Ailerons</strong>, The ailerons are located at the rear of the wing, one or more on each side. They work opposite to each other, so when one is raised, the other is lowered. Their job is to increase the lift on one wing while reducing the lift on the other. By doing this, they roll the aircraft sideways, which allows the aircraft to turn. This is the primary method of steering a fixed-wing aircraft. Modern Large Aircraft has 2 ailerons, one Inboard (near fuselage) and other Outboard (at the end of the wings). Why are the two ailerons provided? Think!
<img src="../img/images/aero/image36.png" class="img-responsive floating-image center">  

<br><br>                                      
Next, <strong>yawing</strong>, done by <strong>rudders</strong>, The rudder is located on the tail of the aircraft. It works identically to a rudder on a boat, steering the nose of the aircraft left and right. Unlike the boat, however, it is not the primary method of steering. Its main purpose is to counteract the drag caused by the lowered aileron during a turn. This adverse yaw, as it is known, causes the nose of the aeroplane to point away, or outwards, from the direction of the turn. The rudder helps to correct this by pushing the nose in the correct direction, maintaining what is known as coordinated flight.
