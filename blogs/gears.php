    <div class="blogPageHeading">GEARS</div>
    <br /><br />

Gears are toothed, mechanical transmission elements used to transfer motion and power between machine components. Operating in mated pairs, gears mesh their teeth with the teeth of another corresponding gear or toothed component which prevents slippage during the transmission process. 
<br /><br />
Gears are generally used for one of four different reasons:<br />
<br />1. To reverse the direction of rotation
<br />2. To increase or decrease the speed of rotation
<br />3. To transmit rotational motion to a different axis
<br />4. To keep the rotation of two axes synchronized
<br /><br />
<div class="blogPageSubHeading">
SPUR GEAR</div><br />
It is one of the simplest gears you will come across.
<br />
<br />* Spur gears transmit power through shafts that are parallel. <br />
<br />* The teeth of the spur gears are parallel to the shaft axis. This causes the gears to produce radial reaction loads on the shaft, but not axial loads. <br />
<img src="../img/images/BME-blog/image32.png" class="img-responsive floating-image center">
<img src="../img/images/BME-blog/image67.gif" class="img-responsive floating-image center">
<br />* While the teeth are rolling through mesh, they roll off of contact with one tooth and accelerate to contact with the next tooth. 
<br /><br />
<div class="blogPageSubHeading">WORM GEAR</div>
<br />
<img src="../img/images/BME-blog/image8.png" class="img-responsive floating-image center">                   
Worm gears are constructed of a worm and a gear (sometimes referred to as a worm wheel), with non-parallel, non-intersecting shafts oriented 90 degrees to each other. The worm is analogous to a screw with a V-type thread, and the gear is analogous to a spur gear. The worm is typically the driving component, with the worm’s thread advancing the teeth of the gear.
<br /><img src="../img/images/BME-blog/image47.gif" class="img-responsive floating-image center">
<br />
<br />* Mainly worm gears are used where there are a high torque and low-speed requirements.
<br />* The meshing of the worm and the gear is a mixture of sliding and rolling actions.
<br /><br />
<div class="blogPageSubHeading">HELICAL GEAR</div>
<br />
<img src="../img/images/BME-blog/image27.jpg" class="img-responsive floating-image center">
<img src="../img/images/BME-blog/image72.jpg" class="img-responsive floating-image center">
<br />
* The teeth of a helical gear are set at an angle (relative to the axis of the gear) and take the shape of a helix. This allows the teeth to mesh gradually, starting as point contact and developing into line contact as the engagement progresses.
<br />
<img src="../img/images/BME-blog/image58.gif" class="img-responsive floating-image center">
<br />

 * One of the most noticeable benefits of helical gears over spur gears is less noise, especially at medium- to high-speeds.
 <br />* Also, with helical gears, multiple teeth are always in mesh, which means less load on each individual tooth.
 <br /><br />
 <div class="blogPageSubHeading">DOUBLE HELICAL GEAR</div>
 <br />
* Imagine two Helical gears joined together such that their teeth look symmetric . That’s when you get a Double Helical gear. From the top, each groove of this gear looks like the letter V, (as can be seen in the picture).
<br />
<img src="../img/images/BME-blog/image22.jpg" class="img-responsive floating-image center">
<img src="../img/images/BME-blog/image53.jpg" class="img-responsive floating-image center">
* The total face is made into two equal halves, both with the same helix angle  but with opposite hands. 
* The axial thrusts are opposite one another, and the forces are contained in the gear and not transmitted to the bearing. Therefore, high loading capability and steady transmission are advantaged.
<br /><br />
<div class="blogPageSubHeading">BEVEL GEAR</div>
<br />
<img src="../img/images/BME-blog/image51.jpg" class="img-responsive floating-image center">
<br />* Bevel gears have a conical form and can be used to transmit rotational power through shafts that are typically at an angle of 90 degrees to each other. Bevel gears are used most frequently for 90° drives, but can be used at other angles as well. A gif is attached below to have an idea of the 90 degree shaft.

<img src="../img/images/BME-blog/image64.gif" class="img-responsive floating-image center">
<br />* Two major differences between bevel gears and spur gears are their shape and the relation of the shafts on which they are mounted. A bevel gear is conical in shape while a spur gear is essentially cylindrical.

<div class="blogPageSubHeading">COMPOUND GEAR</div>
<br />
<img src="../img/images/BME-blog/image38.png" class="img-responsive floating-image center">
<br />* A compound gear is a number of gears fixed together axially. 
<br />* They rotate at the same speed.
<br />* The gears that make up a compound gear usually differ in size and have a different number of teeth. This is useful if there is a need to speed up or slow down the final output.
<br />
<img src="../img/images/BME-blog/image1.gif" class="img-responsive floating-image center">  
<br />* The output gear can be attached to different gears of the compound gears to get a variety of speeds.

