            We know that you have studied it all during your jee. Let's quickly revise it. A battery is a device that
            stores chemical energy, and converts it to electricity. All this is termed as electrochemistry and the
            system that underpins a battery is called an electrochemical cell. Each electrochemical cell consists of two
            electrodes separated by an electrolyte.

            <br>
            So where does an electrochemical cell get its electricity from? To answer this question, we need to know
            what electricity is. Most simply, electricity is a type of energy produced by the flow of electrons. In an
            electrochemical cell, electrons are produced by a chemical reaction that happens at one electrode and then
            they flow over to the other electrode where they are used
            <br>
            <br>
