<div class="blogPageHeading">GEAR RATIO</div>	
<br /> 
Gear ratios are a core science behind almost every machine in the modern era. They can maximize power and efficiency and are based on simple mathematics. So, how do they work? In order to calculate the gear ratio of the two meshed gears we need to know either:
<br /><br />
* the number of teeth of both input and output gears<br />
* the base diameter or radius of both input and output gears
<br /><br />
We will learn it through the teeth method. Let us directly learn it through an example.
<br />
<img src="../img/images/BME-blog/image61.gif" class="img-responsive floating-image center">  
<br />
Here let B be the driving(input) gear and A be the output gear. Then
<br /><br />
                <strong><center>GEAR RATIO = teeth in output gear / teeth in input gear</strong></center>
                <br />
The gear ratio of the above example is 1:2 clearly. Which means the output gear would rotate with the twice angular speed.
<br /><br />
The benefit of knowing the gear ratio is that you can adjust its speed and torque.
<br /><br />
 When the gear ratio is 1:1, the amount of torque is the same, and the speed is the same. <br />
* If you increase the gear ratio (1:4, for example), you're going to cut the amount of torque but significantly increase the amount of speed.<br />
* If you reversed the ratio (say, 4:1), you would cut the speed but boost the torque.
