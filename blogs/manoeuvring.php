We have a three-dimensional world, and thus, we have <strong>three major axes,i.e, lateral, longitudinal, and vertical,</strong> along which motion of an aircraft is controlled (see the section on controlling below). The point of intersection of these three axes gives us the Center of Gravity. The centre of gravity is the point along which the aircraft rotates.
