        <div class="blogPageSubHeading">How does a microprocessor work?</div><br>
        <img src="../img/images/micro/image7.png" class="img-responsive floating-image right" width="400px">

        The microprocessor follows a sequence: Fetch, Decode, Read address from the memory(if needed), and then
        Execute.<br><br>

        Initially, instructions are stored in the memory in sequential order. The microprocessor fetches those
        instructions from the memory, then decodes it, and then if some data from another memory address is needed then
        it reads that data and then executes those instructions and the cycle repeats until STOP instruction is reached.
        Between these processes, the register stores the temporary data, and ALU performs the computations.<br><br>

        Here’s a glossary of some of the frequently used terms in a microprocessor:<br><br>

        Instruction Set: It is the set of instructions that the microprocessor can understand.<br><br>

        Clock Speed: It determines the number of operations the processor can perform per second. It is expressed in
        megahertz (MHz) or gigahertz (GHz). It is also known as Clock Rate.<br><br>

        Data Types: The microprocessor has multiple data type formats like binary, BCD, Hexadecimal.<br><br>

