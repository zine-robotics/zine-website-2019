            <br><br>
            Let’s know about some secondary batteries. But before that let’s study glossary about batteries.
            <br><br>
            Specific Energy: The specific energy of a battery is a measure of how much energy a battery contains in
            comparison to its weight, and is typically expressed in Watt-hours/kilogram (W-hr/kg).

            Energy Density: The energy density of a battery is a measure of how much energy a battery contains in
            comparison to its volume, and is typically expressed in Watt-hours/liter (W-hr/l).
            <br><br>

            Specific Power: Specific power means how much power a battery can deliver in comparison to its weight.
            <br><br>

            Charge/Discharge efficiency: The Charge/Discharge efficiency is the ratio of the energy you can take out of
            a battery divided by the energy you put in.
            <br><br>

            Self-discharge rate: Batteries generate electricity due to a chemical reaction inside the cell. Ideally,
            this happens when it is connected to a device that needs power. However, the reaction could also happen at a
            smaller scale, when the battery’s electrodes are not connected. That means that the battery’s charge
            gradually reduces over time. This phenomenon is called self-discharge and the rate at which it happens is
            called Self-discharge Rate.
            <br><br>

            Nominal Cell Voltage: The average voltage a cell outputs when charged.
            <br><br>

            Cycle Life: The cycle life is the number of complete charge/discharge cycles that the battery is able to
            support before its capacity falls under 80% of its original capacity.
            <br><br>

            Capacity: Capacity of a battery is defined as the number of hours for which a battery can provide a current
            equal to the discharge rate at the nominal voltage of the battery.
            <br><br>
