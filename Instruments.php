      <div class="blogPageSubHeading">PCB</div><br />
      You must have encountered some green, red or blue colored boards inside electronic devices having shiny lines with
      components on them. These are the highly robust and portable circuit boards. They are PCBs or printed circuit
      boards.
      It is a board that has lines and pads that connect various points together. In the picture, there are traces that
      electrically connect the various connectors and components to each other.
      <img src="./img/images/Instruments/image23.png" class="img-responsive floating-image left"> It allows signals and
      power to be routed between physical devices. Solder makes the electrical connections between the surface of the
      PCB and the electronic components. Being metal, solder also serves as a strong mechanical adhesive. A PCB is
      printed with the help of a printing machine but the PCBs are first designed in CAD softwares like EAGLE, KiCAD,
      etc. and then these software aid the printing of PCBs.

      <br /> <br />PCB is composed of four layers usually stacked on one another as shown in left. <br />
      <br /># Substrate is the very base of PCB that provides strength to the PCB and support to its components. It is
      usually made of fiberglass.
      <br /># A layer of copper is fabricated above the substrate of PCB which makes the conductive electrical paths of
      circuit boards. Some boards with dual side circuitry have both sides fabricated with copper layers so that
      components can be soldered on both sides.
      <br /># A solder mask is applied above the copper layer so as to prevent it from shorting from external metals and
      also it acts as a protective mask for copper from being damaged physically and chemically making the circuit path
      undisturbed.
      <br /># A white layer of silk screen is used to print visuals on the circuit board above the solder mask which
      helps the manufacturer or designer to share info about component values or placement directly via board.
