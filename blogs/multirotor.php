<b>Frame: </b> 
<br>
<img src="../img/images/aero/image25.jpg" class="img-responsive floating-image center">

Think of the frame for a drone as a chassis is for a car. The frame is the integral part of the multirotor as it holds all components, so it's necessary for it to be sturdy and rigid. The frame structure varies depending on the multirotor configuration, in the picture above you see a quadcopter’s X frame. Generally it's preferred to have a light and strong frame, so materials like fibre and plastics are used often. Learn <a href="https://www.getfpv.com/learn/new-to-fpv/all-about-multirotor-fpv-drone-frame/">more</a>.
