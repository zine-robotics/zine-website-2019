<div class="blogPageHeading">GEAR TRAINS</div>
<br />
<div class="blogPageSubHeading">Simple Gear Trains:</div>
<img src="../img/images/BME-blog/image55.gif" class="img-responsive floating-image center">  
Here, Gear A is the driving gear and 
Gear B is the driven gear.
When there is only one gear fitted on each shaft, then this type of gearing system is named Simple gear trains. An input shaft rotates the drive gear which then rotates the driven gear  

<br /><br />
<div class="blogPageSubHeading">Compound Gear Trains:</div><br />
<img src="../img/images/BME-blog/image36.gif" class="img-responsive floating-image center"><br /> 
Simply put when there are more than 2 gears in a gear train it's referred to as a compound gear train.  When we need a higher speed ratio or a much lower speed ratio then we need to use compound gear trains. As compared to simple gear train, if one were to use it as a reduction mechanism i.e. Lower the speed of the input shaft by a ratio of 10 (suppose), then the mechanism becomes very large, the driven gear needs to have a circumference 10 times that of driving gear. The compound gear train allows to achieve much higher gear ratios in a compact setup.
<br /><br />
<div class="blogPageSubHeading">Epicyclic Gear Trains:</div><br />
<img src="../img/images/BME-blog/image3.gif" class="img-responsive floating-image center"> <br />
<strong>Components of Epicyclic Gearbox</strong><br />
<br /><strong>Sun Gear -</strong> Yellow Color
<br /><strong>Planet Gears -</strong> Blue Color
<br /><strong>Ring Gear -</strong> Red Color
<br /><strong>Planet Carrier -</strong> Green Color
<br /><br />
The Sun gear is where the input shaft power is given and the ring gear is the output shaft power is produced. The epicyclic gear train is useful for transmitting high-velocity ratio with a gear of moderate size and in less space. It is used in Automatic Gear Transmission Cars, and wind turbines.
