The construction and control is easy for multirotors and are often used in remotely controlled drones, and aerial vehicle projects, and are easily configurable to equip more weight carrying capacity or thrust to weight ratio. Another use case for multi rotors is aerial mapping and inspection systems, their hovering ability allows it to be very useful in such cases. 
<br> But they are not exactly all good either: Cons. Because of the multi rotor setup the flight times for the craft are rather short as the battery drains faster. As compared to a fixed wing aircraft the payload capacity is also smaller.
<br><br>
As we know Quadcopters make use of 4 Motors. So, Two of these motors spin clockwise while the other two spin counterclockwise. Motors on the same axis spin in the same direction, as illustrated here. <b>Altitude</b> is a term that you’re probably familiar with – it simply describes going up or down. A quadcopter can either hover or adjust its altitude by applying equal thrust to all four rotors.
 <br><br>
 <img src="../img/images/aero/image4.png" class="img-responsive floating-image left" width="450px">
 <img src="../img/images/aero/image30.png" class="img-responsive floating-image right" width="450px">

To adjust its <b>Yaw</b>, or make it turn left or right, the quadcopter applies more thrust to one set of motors [image on the right]. For example, a quadcopter may apply more thrust to the two motors that spin clockwise to make a turn. This happens because the Angular momentum generated by the propellers provides a torque to the body of the quadcopter. The torque is cancelled out when all motors run at the same speeds. 
<img src="../img/images/aero/image4.png" class="img-responsive floating-image left">

<b>Pitch and roll</b>  on the other hand are adjusted by applying more thrust on one rotor and less to the other opposing rotor. For example, the quadcopter can adjust its pitch by applying more thrust to the clockwise spinning motor in the front and less thrust to the clockwise spinning motor directly opposite in the back.
<br> One thing that makes quadcopters easy to fly is that you don’t actually have to adjust the motor speeds manually as this is where the controller and electronics come in. The mechanics of flight of a quadcopter make it agile as compared to a fixed-wing aircraft. This allows doing manoeuvres which are not possible in any other form of flight, Zine Drone Racing explores these avenues. 
