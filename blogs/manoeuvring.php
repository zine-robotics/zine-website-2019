        <div class="blogPageHeading">Center of gravity & Concept of stability:
        </div>
<br>
Ever wondered what would happen when you throw your first built paper plane, which is not controlled by you, there is no one sitting on it, and you just throw it? <br>
Well, after you let go of that plane, it completely depends on the build of your plane how it goes, it will have to face various <strong>disturbances</strong>, and <strong>how your plane reacts</strong>  to it depends on its <strong>design</strong>, and that reaction determines its stability.
 <br><br>
There are two types of stability we will discuss, one is <strong>static stability</strong>, which is the initial tendency of aircraft to return to its position, completely dependent on its design. While <strong>Dynamic stability</strong> is the stability over the course of time. So, not just intention, not just tendency, but coming back so the outcome of disturbing an aircraft depends on its dynamic stability. There are also other types of stability, roll stability, in yaw, in pitch, of course, we want to cover this type of stability too, but it will start to get boring and a little off-topic. We’ll not go into depth as of now, though you are encouraged to learn further about it <a href="https://drive.google.com/file/d/1Jk2GVwEXlz2zK-hDHXUlYVaDtTNeP4tT/view">here</a>.
<br><br>
Intuitively, you might want to balance to maintain stability, so how should we balance? Like Thanos?
But we don’t want to go intuitively, so we define three major points,i.e, CG, CP, Neutral Point. Let’s start with the <strong>centre of gravity.</strong> We won’t discuss the other two points here. 
The center of gravity is the average location of the weight of the aircraft. The weight is distributed throughout the airplane, and for some problems, it is important to know the distribution. But for total aircraft maneuvering, we need to be concerned with only the total weight and the location of the center of gravity. 
 
